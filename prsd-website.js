/* ══════════════════════════════════════════════════════════════
   HAMBURGER MENU
══════════════════════════════════════════════════════════════ */
const ham  = document.getElementById('ham');
const mob  = document.getElementById('mobNav');

ham.addEventListener('click', () => {
  const open = mob.classList.toggle('open');
  ham.setAttribute('aria-expanded', open);
  mob.setAttribute('aria-hidden', !open);
  document.body.style.overflow = open ? 'hidden' : '';
});

function closeMob() {
  mob.classList.remove('open');
  ham.setAttribute('aria-expanded', 'false');
  mob.setAttribute('aria-hidden', 'true');
  document.body.style.overflow = '';
}

document.addEventListener('click', e => {
  if (mob.classList.contains('open') && !mob.contains(e.target) && !ham.contains(e.target)) {
    closeMob();
  }
});

document.addEventListener('keydown', e => {
  if (e.key === 'Escape' && mob.classList.contains('open')) closeMob();
});

/* ══════════════════════════════════════════════════════════════
   SCROLL REVEAL (IntersectionObserver)
══════════════════════════════════════════════════════════════ */
const revObs = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (!entry.isIntersecting) return;
    const delay = entry.target.dataset.delay ? entry.target.dataset.delay + 'ms' : '0ms';
    entry.target.style.transitionDelay = delay;
    entry.target.classList.add('in');
    revObs.unobserve(entry.target);
  });
}, { threshold: 0.08, rootMargin: '0px 0px -40px 0px' });

document.querySelectorAll('.reveal').forEach(el => revObs.observe(el));

/* ══════════════════════════════════════════════════════════════
   LANGUAGE SYSTEM
══════════════════════════════════════════════════════════════ */
let lang = 'en';

function applyLang(l) {
  if (!T[l]) return;
  lang = l;
  const isRTL = l === 'ar';

  document.documentElement.lang = l;
  document.documentElement.dir  = isRTL ? 'rtl' : 'ltr';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = T[l][el.dataset.i18n];
    if (v !== undefined) el.textContent = v;
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const v = T[l][el.dataset.i18nHtml];
    if (v !== undefined) setHtml(el, v);
  });

  document.querySelectorAll('.lang-btn').forEach(btn => {
    const active = btn.dataset.lang === l;
    btn.setAttribute('aria-pressed', active);
  });

  const track = document.getElementById('mqTrack');
  if (track) track.style.animationName = isRTL ? 'mqRTL' : 'mqLTR';

  const titles = {
    en: "PRS'D — Euro-Street Food Made with Love",
    ar: "PRS'D — أكل شوارع أوروبي مصنوع بحب",
    fr: "PRS'D — Street Food Fait avec Amour",
  };
  document.title = titles[l] || titles.en;

  try { localStorage.setItem('prsd-lang', l); } catch(e) {}
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => applyLang(btn.dataset.lang));
});

const saved = (() => { try { return localStorage.getItem('prsd-lang'); } catch(e) { return null; } })();
applyLang(T[saved] ? saved : 'en');

/* ══════════════════════════════════════════════════════════════
   SMOOTH SCROLL (nav-height offset)
══════════════════════════════════════════════════════════════ */
document.querySelectorAll('a[href^="#"]').forEach(a => {
  a.addEventListener('click', function(e) {
    const id = this.getAttribute('href');
    if (id === '#') return;
    const target = document.querySelector(id);
    if (!target) return;
    e.preventDefault();
    const navH = parseInt(getComputedStyle(document.documentElement).getPropertyValue('--nav-h')) || 68;
    window.scrollTo({ top: target.getBoundingClientRect().top + window.scrollY - navH, behavior: 'smooth' });
    closeMob();
  });
});

